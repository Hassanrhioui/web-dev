// Function to display a greeting message
function greet(name) {
    return `Hello, ${name}! Welcome to page 2.`;
}

// Example usage
const userName = 'Hassan';
console.log(greet(userName));

// Function to add two numbers
function add(a, b) {
    return a + b;
}

// Example usage
const num1 = 5;
const num2 = 10;
console.log(`The sum of ${num1} and ${num2} is ${add(num1, num2)}.`);